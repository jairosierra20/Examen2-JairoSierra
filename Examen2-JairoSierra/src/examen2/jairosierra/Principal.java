    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
        // TODO add your handling code here:
        try
        {
            jt_partes.setModel(new javax.swing.table.DefaultTableModel(
            new Object [][] {

            },
            new String [] {
                "Parte", "Tiempo"
            }
        ) {
            Class[] types = new Class [] {
                java.lang.Integer.class, java.lang.String.class, java.lang.Integer.class, java.lang.Object.class
            };
            boolean[] canEdit = new boolean [] {
                false, false, false, false
            };

            public Class getColumnClass(int columnIndex) {
                return types [columnIndex];
            }

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit [columnIndex];
            }
        });
        DefaultTableModel modelo = (DefaultTableModel) jt_partes.getModel();
        Computadora x = lista.get(cb_compus.getSelectedIndex());
        for (int i = 0; i < x.getPartes().size(); i++) {
            HT = new simulacion(pg_t,Integer.parseInt(x.getPartes().get(i).getTiempo()));
                    HT.setNumero(Integer.parseInt(x.getPartes().get(i).getTiempo()));
                    HT.start();
                    HT.setAvanzar(true);
                Object[] newrow = {
                    x.getPartes().get(i), x.getPartes().get(i).getTiempo()
                };
                modelo.addRow(newrow);
                jt_partes.setModel(modelo);
        }
        Tecnicos c = (Tecnicos)cb_tecnicos.getSelectedItem();
        lista4.add(x);
        lista5.add(c);
        if(lista4.size()<=5 || lista4.size()>=1){
            double r = lista4.size()*0.30;
            double t = lista4.size()-r;
            int d = (int)t;
            for (int i = 0; i < d; i++) {
                lista4.remove(i);
                fallidas.add(lista4.get(i));
            }
        }
        if(lista4.size()<=15 || lista4.size()>=6){
            double r = lista4.size()*0.22;
            double t = lista4.size()-r;
            int d = (int)t;
            for (int i = 0; i < d; i++) {
                lista4.remove(i);
                fallidas.add(lista4.get(i));
            }
        }
        if(lista4.size()<=30 || lista4.size()>=16){
            double r = lista4.size()*0.13;
            double t = lista4.size()-r;
            int d = (int)t;
            for (int i = 0; i < d; i++) {
                lista4.remove(i);
                fallidas.add(lista4.get(i));
            }
        }
        if(lista4.size()>=30){
            double r = lista4.size()*0.07;
            double t = lista4.size()-r;
            int d = (int)t;
            for (int i = 0; i < d; i++) {
                lista4.remove(i);
                fallidas.add(lista4.get(i));
            }
            c.setEnsambladas(lista4.size());
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Ingrese bien los datos del tiempo");
        }
    }//GEN-LAST:event_jButton15ActionPerformed

